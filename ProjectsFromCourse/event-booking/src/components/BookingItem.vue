<template>
    <SectionCard>
        <div class="flex justify-between">
            <div>
                {{ title }}
            </div>
            <div class="spa">
                <component :is="icon" :class="{'animate-spin':pending}"/>
            </div>
            <RegisteerButton variant="danger" @click="$emit('cancelled')">cancel</RegisteerButton>
        </div>
    </SectionCard>
</template>

<script setup>
import { computed } from 'vue'
import SectionCard from './SectionCard.vue';
import RegisteerButton from './RegisteerButton.vue';
import { LoaderCircle , Check } from 'lucide-vue-next';

const props = defineProps({
    title: String,
    status: String,
    buttonRegister: String
})
const pending = computed(()=> props.status === 'pending')
const icon = computed(()=> pending.value? LoaderCircle : Check)
defineEmits(['cancelled'])
</script>

